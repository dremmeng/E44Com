def _crystal_idx_by_weight(W_mod, wt):
    """
    Return the unique crystal basis index in W_mod with Dynkin weight tuple wt.

    Parameters
    ----------
    W_mod : WHat4Module
    wt    : tuple  (h_1, h_2, h_3)  Dynkin weight

    Raises ValueError if the weight slot is absent or not one-dimensional.
    """
    ws = W_mod.weight_spaces.get(wt, [])
    if len(ws) != 1:
        raise ValueError(
            f"Expected a unique crystal element with Dynkin weight {wt} "
            f"in W_hat({W_mod.t},{W_mod.a},{W_mod.b},{W_mod.c}); "
            f"found {len(ws)}: {ws}"
        )
    return ws[0]


def _joint_kernel_deg1(M, e44_data):
    """
    Compute the joint kernel of all L_1 generators acting on M[deg=1].

    Each L_1 generator Y gives a matrix mats[1]: M[1] \to M[0].
    The joint kernel is  { v \in M[1] : Y\cdotv = 0 for all Y \in L_1 }.

    Parameters
    ----------
    M        : VermaModule  (only degrees 0 and 1 are used)
    e44_data : dict from load_e44()

    Returns
    -------
    list of QQ vectors spanning the joint kernel.

    Performance note
    ----------------
    Uses a lightweight M with max_deg=1 to avoid computing matrices at
    higher degrees inside l0_action_matrix / l1_action_matrix.
    """
    L1   = e44_data['E44'][1]
    # Lightweight module: only degrees 0..1 needed
    _e44 = getattr(M, '_e44_data', None)
    M1   = M_verma(M.t, M.a, M.b, M.c, max_deg=1, e44_data=_e44)
    n    = M1.dim(1)
    mats = []
    for i1 in range(len(L1)):
        mat = l1_action_matrix(M1, i1, e44_data)[1]   # M[1] \to M[0]
        if mat.nrows() > 0 and mat.ncols() > 0:
            mats.append(mat)

    if not mats:
        from sage.all import identity_matrix
        return list(identity_matrix(QQ, n).rows())

    # Stack all matrices vertically
    total_rows = sum(m.nrows() for m in mats)
    stacked = matrix(QQ, total_rows, n)
    r = 0
    for m in mats:
        for row_idx in range(m.nrows()):
            stacked[r] = m.row(row_idx)
            r += 1

    return list(stacked.right_kernel().basis())


def annihilator_check(v, M, e44_data, deg=1, verbose=False):
    """
    Verify that v \in M[deg] is annihilated by every L_1 generator.

    Parameters
    ----------
    v        : QQ vector of length M.dim(deg)
    M        : VermaModule
    e44_data : dict from load_e44()
    deg      : degree of v in M (default 1)
    verbose  : if True, print which generators fail

    Returns
    -------
    bool — True iff L_1_i \cdot v = 0 for all L_1 generators i.

    Performance note
    ----------------
    l1_action_matrix (and the l0_action_matrix it calls internally) scales
    with M.max_deg.  For an annihilator check at degree ``deg`` we only need
    actions at degrees deg and deg-1, so we create a lightweight copy of M
    with max_deg = deg to avoid computing large matrices at higher degrees.
    """
    # Lightweight module: only degrees 0..deg are needed.
    _e44 = getattr(M, '_e44_data', None)
    M_fast = M_verma(M.t, M.a, M.b, M.c, max_deg=deg, e44_data=_e44)

    L1       = e44_data['E44'][1]
    all_zero = True
    zero_dm1 = M.zero_vec(deg - 1)

    for i1 in range(len(L1)):
        mats = l1_action_matrix(M_fast, i1, e44_data)
        if deg < len(mats) and mats[deg] is not None:
            result = mats[deg] * v
            if result != zero_dm1:
                all_zero = False
                if verbose:
                    print(
                        f"  NOT annihilated by L_1[{i1}]"
                        f" (label={L1[i1]['label']}, parity={L1[i1]['parity']})"
                    )
    return all_zero



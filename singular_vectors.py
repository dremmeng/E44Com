def phi_1E(e44_data, max_source_deg=1, src_e44_data=None):
    """
    Morphism \phi[1E]: M_0(0,0,0) \to M_1(1,0,0), degree 1.
    Requires \hat{p}(4) fiber.
    """
    M_tar = M_verma(1, 1, 0, 0, max_deg=1 + max_source_deg,
                     e44_data=e44_data)
    ker = _joint_kernel_deg(M_tar, e44_data, deg=1)

    # The source W_0(0,0,0) is the trivial \hat{p}(4)-module (ALL of L_0 acts
    # as zero).  So the Verma-module-map image w[1E] must satisfy both:
    #   (a) L_1 \cdot w = 0   (already in `ker`)
    #   (b) L_0 \cdot w = 0   (module-map equivariance for trivial source)
    # Intersect the L_1 kernel with the joint L_0 kernel at degree 1.
    L0 = e44_data['E44'][0]
    K_mat = matrix(QQ, ker)                       # rows = kernel vectors
    for l0_idx in range(len(L0)):
        l0_mat = l0_action_matrix(M_tar, l0_idx, e44_data, max_d=1)[1]
        images = K_mat * l0_mat.transpose()       # each row \to L_0\cdot(ker vec)
        # Find sub-kernel: vectors in ker annihilated by this L_0 gen
        sub_ker = images.left_kernel().basis()
        if not sub_ker:
            raise RuntimeError(
                f"w[1E]: L_0[{l0_idx}] annihilates no kernel vectors"
            )
        K_mat = matrix(QQ, [sum(c * vector(QQ, ker[i])
                                for i, c in enumerate(row))
                            for row in sub_ker])
        ker = list(K_mat.rows())
    if len(ker) != 1:
        raise RuntimeError(
            f"Expected 1-dim joint (L_1 ∩ L_0) kernel for w[1E]; "
            f"got {len(ker)}"
        )
    w = ker[0]
    first_nz = next(c for c in w if c != QQ(0))
    w = (QQ(1) / first_nz) * w

    M_src = M_verma(0, 0, 0, 0, max_deg=max_source_deg)
    phi0 = [vector(QQ, w)]

    matrices = {}
    for d in range(max_source_deg + 1):
        matrices[d] = _phi_at_degree(M_src, M_tar, 1, phi0, d)
    return M_src, M_tar, 1, phi0, matrices



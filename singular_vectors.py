def save_morphisms(filepath, e44_data, max_deg=4, verbose=True):
    """
    S3.7: Compute and export all morphism data needed for the de Rham
    complexes (S4) to a pickle file.

    For each morphism type, stores:
      - Source/target module parameters (t, a, b, c)
      - Singular vector degree
      - phi0 vectors (fiber map: list of QQ vectors)
      - Morphism matrices at each source degree 0..max_source_deg
      - Source/target module dimensions at each degree

    The max_source_deg for each morphism is chosen so that the target
    degree does not exceed max_deg.

    Parameters
    ----------
    filepath : str
        Output pickle file path.
    e44_data : dict
        E(4,4) bracket data from load_e44().
    max_deg : int
        Maximum target degree (default 4).  Morphism matrices are computed
        for source degrees d such that d + sv_deg <= max_deg.
    verbose : bool
        Print progress messages.
    """
    if e44_data is None:
        raise ValueError("e44_data is required for morphism export")

    data = {
        'max_deg': max_deg,
        'morphisms': {},
        'singular_vectors': {},
    }

    def _export_morphism(key, phi_func, phi_args, sv_deg_expected,
                         max_source_deg_override=None):
        """Compute one morphism and store its data."""
        if verbose:
            print(f"  Computing {key} ...")
        max_source_deg = max_deg - sv_deg_expected
        if max_source_deg_override is not None:
            max_source_deg = min(max_source_deg, max_source_deg_override)
        if max_source_deg < 0:
            if verbose:
                print(f"    (skipped: max_deg={max_deg} < sv_deg={sv_deg_expected})")
            return
        M_src, M_tar, sv_deg, phi0, matrices = phi_func(
            *phi_args, max_source_deg=max_source_deg
        )
        # Store phi0 as list of plain vectors
        phi0_data = [list(v) if v is not None else None for v in phi0]
        # Store matrices as-is (SageMath QQ matrices pickle natively)
        mat_data = {d: matrices[d] for d in sorted(matrices)}
        # Module dimension tables
        src_dims = {d: M_src.dim(d) for d in range(max_source_deg + 1)}
        tar_dims = {d: M_tar.dim(d)
                    for d in range(sv_deg + max_source_deg + 1)}

        data['morphisms'][key] = {
            'label': key,
            'src_params': (M_src.t, M_src.a, M_src.b, M_src.c),
            'tar_params': (M_tar.t, M_tar.a, M_tar.b, M_tar.c),
            'sv_deg': sv_deg,
            'phi0': phi0_data,
            'matrices': mat_data,
            'src_dims': src_dims,
            'tar_dims': tar_dims,
            'src_dim_W': M_src.dim_W,
            'tar_dim_W': M_tar.dim_W,
        }

    def _export_sv(key, sv_func, sv_args):
        """Compute one singular vector and store it."""
        M, v = sv_func(*sv_args)
        deg = next(d for d in range(1, 5)
                   if M.dim(d) == len(v))
        data['singular_vectors'][key] = {
            'params': (M.t, M.a, M.b, M.c),
            'deg': deg,
            'vec': list(v),
            'module_dim_W': M.dim_W,
        }

    if verbose:
        print("─" * 60)
        print("s3.7 — Exporting morphism data")
        print("─" * 60)

    # ── Singular vectors ───────────────────────────────────────────────
    if verbose:
        print("\n  Singular vectors:")
    _export_sv('w1A_0_1', w1A, (QQ(0), 1))
    _export_sv('w1A_2_1', w1A, (QQ(2), 1))
    _export_sv('w1B_1_1', w1B, (QQ(1), 1))
    _export_sv('w1B_2_2', w1B, (QQ(2), 2))
    _export_sv('w1C_1',   w1C, (QQ(1), e44_data))
    _export_sv('w1D_1',   w1D, (QQ(1), e44_data))
    _export_sv('w1D_3',   w1D, (QQ(3), e44_data))
    _export_sv('w1E',     w1E, (QQ(1),))
    _export_sv('w3F',     w3F, (e44_data,))
    _export_sv('w3G',     w3G, (e44_data,))
    _export_sv('w4H_1',   w4H, (QQ(1), e44_data))
    _export_sv('w4H_3',   w4H, (QQ(3), e44_data))
    if verbose:
        print(f"    {len(data['singular_vectors'])} singular vectors stored")

    # ── Morphisms needed for Complex I (Figure 7 of CKC 2026) ─────────
    # \phi[1A]: M_{t-1}(a+1,0,0) \to M_t(a,0,0)
    if verbose:
        print("\n  Morphisms (Complex I):")
    _export_morphism('phi_1A_0_1',
                     phi_1A, (QQ(0), 1, e44_data), sv_deg_expected=1)
    _export_morphism('phi_1A_1_0',
                     phi_1A, (QQ(1), 0, e44_data), sv_deg_expected=1)
    _export_morphism('phi_1A_2_1',
                     phi_1A, (QQ(2), 1, e44_data), sv_deg_expected=1)
    # \phi[1B]: M_{t-1}(0,0,c-1) \to M_t(0,0,c)
    _export_morphism('phi_1B_1_1',
                     phi_1B, (QQ(1), 1, e44_data), sv_deg_expected=1)
    _export_morphism('phi_1B_2_1',
                     phi_1B, (QQ(2), 1, e44_data), sv_deg_expected=1)
    _export_morphism('phi_1B_2_2',
                     phi_1B, (QQ(2), 2, e44_data), sv_deg_expected=1)
    # \phi[1C]: M_{t-1}(0,1,0) \to M_t(0,0,1)  [\hat{p}(4) target fiber dim=80; cap deg]
    _export_morphism('phi_1C_1',
                     phi_1C, (QQ(1), e44_data), sv_deg_expected=1,
                     max_source_deg_override=1)
    # \phi[1D]: M_{t-1}(1,0,0) \to M_t(0,0,0)
    _export_morphism('phi_1D_1',
                     phi_1D, (QQ(1), e44_data), sv_deg_expected=1)
    _export_morphism('phi_1D_2',
                     phi_1D, (QQ(2), e44_data), sv_deg_expected=1)
    _export_morphism('phi_1D_3',
                     phi_1D, (QQ(3), e44_data), sv_deg_expected=1)
    # \phi[1E]: M_0(0,0,0) \to M_1(1,0,0)  (unique: only at t=1)
    _export_morphism('phi_1E',
                     phi_1E, (e44_data,), sv_deg_expected=1)

    # ── Morphisms needed for Complex II (Figure 8 of CKC 2026) ────────
    if verbose:
        print("\n  Morphisms (Complex II):")
    # \phi[3F]: M_0(0,0,0) \to M_3(1,0,0)
    _export_morphism('phi_3F',
                     phi_3F, (e44_data,), sv_deg_expected=3)
    # \phi[3G]: M_{-3}(1,0,0) \to M_0(0,0,0)
    _export_morphism('phi_3G',
                     phi_3G, (e44_data,), sv_deg_expected=3)
    # \phi[4H]: M_{t-4}(1,0,0) \to M_t(1,0,0)
    _export_morphism('phi_4H_1',
                     phi_4H, (QQ(1), e44_data), sv_deg_expected=4)
    _export_morphism('phi_4H_3',
                     phi_4H, (QQ(3), e44_data), sv_deg_expected=4)

    # ── Write pickle ───────────────────────────────────────────────────
    with open(filepath, 'wb') as f:
        _pickle.dump(data, f, protocol=4)

    if verbose:
        n_morph = len(data['morphisms'])
        n_sv = len(data['singular_vectors'])
        n_mats = sum(len(m['matrices']) for m in data['morphisms'].values())
        fsize = _os.path.getsize(filepath)
        print(f"\n  Wrote {filepath}")
        print(f"    {n_morph} morphisms, {n_sv} singular vectors,"
              f" {n_mats} matrices")
        print(f"    File size: {fsize:,} bytes")



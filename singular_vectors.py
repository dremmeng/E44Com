#
# w[3_G] \in M_0(0,0,0)[deg=3]  (sl_4 fiber, dim_W=1)
#   Formula: (\Sigma_i \partial_id_i) d_1 \otimes 1  =  -(\partial_2d_1d_2 + \partial_3d_1d_3 + \partial_4d_1d_4) \otimes 1
#   (The i=1 term vanishes since d_1^2 = 0.)
#
# w[3_F] \in M_3(1,0,0)[deg=3]  (\hat{p}(4) fiber W_3(1,0,0), dim_W=8)
#   Constructed from the joint L_1-kernel: the unique (up to scalar)
#   primitive singular vector at degree 3 in M_3(1,0,0) with full
#   \hat{p}(4)-fiber.
#
# Morphisms:
#   \phi[3_G]: M_{-3}(1,0,0) \to M_0(0,0,0),  v ↦ w[3_G] \cdot v
#   \phi[3_F]: M_0(0,0,0) \to M_3(1,0,0),      v ↦ w[3_F] \cdot v
# ---------------------------------------------------------------------------


def w3G(e44_data=None):
    """
    Degree-3 singular vector w[3_G] \in M_0(0,0,0)[deg=3].

    Formula (CKC 2026):  w[3_G] = (\Sigma_i \partial_i d_i) d_1 \otimes 1

    In PBW normal form (d_1^2 = 0 kills the i=1 term):
        -\partial_2 d_1 d_2 \otimes 1  -  \partial_3 d_1 d_3 \otimes 1  -  \partial_4 d_1 d_4 \otimes 1

    Returns
    -------
    (M, v) where M = VermaModule M_0(0,0,0), v \in M[3].
    """
    M = M_verma(0, 0, 0, 0, max_deg=3)
    coeffs = {}
    # \partial_{i+1} d_1 d_{i+1}  for i = 1,2,3  (gen indices)
    # PBW: \alpha with \partial_{gen} = 1, S = {0, gen}
    for gen in [1, 2, 3]:
        alpha = [0, 0, 0, 0]
        alpha[gen] = 1
        mon = (tuple(alpha), frozenset({0, gen}))
        j = M._pbw_idx[3][mon]
        coeffs[(j, 0)] = QQ(-1)
    return M, M.to_vec(3, coeffs)


def w3F(e44_data):
    """
    Degree-3 singular vector w[3_F] \in M_3(1,0,0)[deg=3].

    Requires the full \hat{p}(4)-fiber W_3(1,0,0) (dim=8).
    Constructed from the 1-dimensional joint L_1-kernel at degree 3.

    Parameters
    ----------
    e44_data : dict from load_e44()

    Returns
    -------
    (M, v) where M = VermaModule M_3(1,0,0) with phat4 fiber, v \in M[3].
    """
    M = M_verma(3, 1, 0, 0, max_deg=3, e44_data=e44_data)
    ker = _joint_kernel_deg(M, e44_data, deg=3)
    if len(ker) != 1:
        raise RuntimeError(
            f"Expected 1-dim kernel at degree 3 in M_3(1,0,0) with phat4 "
            f"fiber; got {len(ker)}"
        )
    return M, ker[0]



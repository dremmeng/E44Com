# ===========================================================================
# s3.5 — Verma module morphisms  \phi[1A] - \phi[4H]
# ===========================================================================
#
# A singular vector w \in M_t(a,b,c)[k] of sl_4 weight (a',b',c') defines a
# unique E(4,4)-module morphism
#
#     \phi: M_{t-k}(a',b',c') \to M_t(a,b,c),    \phi(v_hw) = w.
#
# The morphism extends by the universal property of Verma modules:
#     \phi(g \otimes v') = g \cdot \phi_0(v')    for  g \in U(L_{<0}),  v' \in W_{t-k}(a',b',c').
#
# The fiber map \phi_0: W_source \to M_target[k] is the unique L_0-module map
# with \phi_0(v_hw) = w, computed by BFS through L_0 lowering operators.
#
# Morphism catalogue:
#   \phi[1A]: M_{t-1}(a+1,0,0) \to M_t(a,0,0),       degree 1  (a>=1 or a=t=0)
#   \phi[1B]: M_{t-1}(0,0,c-1) \to M_t(0,0,c),        degree 1  (c>=1)
#   \phi[1C]: M_{t-1}(0,1,0)   \to M_t(0,0,1),         degree 1  (all t)
#   \phi[1D]: M_{t-1}(1,0,0)   \to M_t(0,0,0),         degree 1  (t!=0)
#   \phi[1E]: M_0(0,0,0)       \to M_1(1,0,0),          degree 1  (phat4 fiber)
#   \phi[3F]: M_0(0,0,0)       \to M_3(1,0,0),          degree 3  (phat4 fiber)
#   \phi[3G]: M_{-3}(1,0,0)    \to M_0(0,0,0),          degree 3
#   \phi[4H]: M_{t-4}(1,0,0)   \to M_t(1,0,0),          degree 4  (sl_4 source fiber)
# ---------------------------------------------------------------------------

# sl_4 lowering operators f_r = E_{r+1,r}  \to  L_0 index  (see _w_action_from_l0_idx)
#   f_1 = E_2_1: L0_idx = 8
#   f_2 = E_3_2: L0_idx = 5
#   f_3 = E_4_3: L0_idx = 2
_SL4_LOWER_L0 = {1: 8, 2: 5, 3: 2}


def _compute_phi0(w, M_target, sv_deg, W_source, e44_data):
    """
    Compute the fiber map \phi_0: W_source \to M_target[sv_deg].

    \phi_0 is the unique L_0-module map with \phi_0(v_hw) = w.
    Computed via BFS through crystal lowering (sl_4 fiber) or
    full L_0 orbit (\hat{p}(4) fiber).

    Returns list of dim(W_source) vectors in M_target[sv_deg].
    """
    n_src = W_source.dim
    phi0 = [None] * n_src
    phi0[W_source.v_hw] = vector(QQ, w)

    if n_src == 1:
        return phi0

    # Determine generators for BFS
    if isinstance(W_source, Phat4Module):
        gen_indices = sorted(W_source.action_mats.keys())
        fiber_acts = W_source.action_mats
        # Even indices (< 16): sl_4 subalgebra of \hat{p}(4)
        # Odd indices (>= 16): odd part of \hat{p}(4)
        even_gen_indices = [i for i in gen_indices if i < 16]
    else:
        # sl_4 crystal fiber — use lowering operators
        gen_indices = [_SL4_LOWER_L0[r] for r in [1, 2, 3]]
        fiber_acts = {_SL4_LOWER_L0[r]: W_source.f_mats[r]
                      for r in [1, 2, 3]}
        even_gen_indices = gen_indices

    # Precompute L_0 action on M_target[sv_deg]
    l0_M = {}
    for h_idx in gen_indices:
        l0_M[h_idx] = l0_action_matrix(M_target, h_idx, e44_data, max_d=sv_deg)[sv_deg]

    # Phase 1: BFS with sole-unknown heuristic.
    # For \hat{p}(4) fibers, use ONLY even generators (sl_4 part): these
    # correctly find the V(a,b,c) orbit.  Odd generators couple V(a,b,c)
    # with V(c,b,a) and the sole-unknown heuristic can assign incorrect
    # zeros.  The odd-sector unknowns are deferred to Phase 2.
    bfs_gens = even_gen_indices if isinstance(W_source, Phat4Module) \
        else gen_indices
    changed = True
    while changed:
        changed = False
        for h_idx in bfs_gens:
            h_W = fiber_acts[h_idx]
            for k in range(n_src):
                if phi0[k] is None:
                    continue
                h_col = h_W.column(k)
                for k_prime in range(n_src):
                    if phi0[k_prime] is not None or h_col[k_prime] == 0:
                        continue
                    unknowns = [kp for kp in range(n_src)
                                if h_col[kp] != 0 and phi0[kp] is None]
                    if len(unknowns) != 1:
                        continue
                    rhs = l0_M[h_idx] * phi0[k]
                    for kp in range(n_src):
                        if kp != k_prime and h_col[kp] != 0:
                            if phi0[kp] is not None:
                                rhs = rhs - h_col[kp] * phi0[kp]
                    phi0[k_prime] = (QQ(1) / h_col[k_prime]) * rhs
                    changed = True

    n_found = sum(1 for v in phi0 if v is not None)
    if n_found == n_src:
        return phi0

    # Phase 2: Full linear system for remaining unknowns.
    # L_0 equivariance:  l0_M[h] * \phi_0(e_k) = \Sigma_{k'} h_W[k',k] * \phi_0(e_{k'})
    # For known k and unknown k' indices, this gives a linear system.
    #
    # Key insight: the system decouples per target-coordinate.  For each
    # coordinate i \in {0,...,m-1}, the unknowns are {\phi_0(e_{k'})[i] : k'\inU}
    # and each (h, k_known) constraint contributes one scalar equation:
    #   \Sigma_{k'\inU} h_W[k',k] * \phi_0(e_{k'})[i] = rhs(h,k)[i]
    # The coefficient matrix A is the same for all i; solve A\cdotX = B where
    # X[:,i] = (\phi_0(e_{u_0})[i], \phi_0(e_{u_1})[i], ...).
    unknown_idx = [k for k in range(n_src) if phi0[k] is None]
    known_idx = [k for k in range(n_src) if phi0[k] is not None]
    n_unk = len(unknown_idx)
    unk_map = {k: j for j, k in enumerate(unknown_idx)}
    m = len(w)

    # Build coefficient matrix A (rows = constraints, cols = unknowns)
    # and RHS matrix B (rows = constraints, cols = m target coordinates)
    A_rows = []
    B_rows = []
    for h_idx in gen_indices:
        h_W = fiber_acts[h_idx]
        for k in known_idx:
            h_col = h_W.column(k)
            # Check if this constraint involves any unknown
            has_unk = any(h_col[kp] != 0 for kp in unknown_idx)
            if not has_unk:
                continue
            # Coefficient row: c_{j} = h_W[unknown_idx[j], k]
            a_row = [h_col[kp] for kp in unknown_idx]
            # RHS: l0_M[h]*phi0[k] - \Sigma_{known k''} h_W[k'',k]*phi0[k'']
            rhs = l0_M[h_idx] * phi0[k]
            for k2 in known_idx:
                if h_col[k2] != 0:
                    rhs = rhs - h_col[k2] * phi0[k2]
            A_rows.append(a_row)
            B_rows.append(rhs)

    if not A_rows:
        raise RuntimeError(
            f"L_0 orbit BFS reached {n_found}/{n_src} fiber elements; "
            f"no constraints link remaining unknowns"
        )

    A = matrix(QQ, A_rows)
    B = matrix(QQ, [list(v) for v in B_rows])  # n_constraints × m

    # Solve A * X = B  where X is n_unk × m  (each row = one unknown \phi_0)
    # Use A's right-nullspace and particular solution.
    try:
        X = A.solve_right(B)
    except ValueError:
        raise RuntimeError(
            f"L_0 equivariance system inconsistent for "
            f"{n_unk} unknown fiber elements"
        )

    for j, k in enumerate(unknown_idx):
        phi0[k] = vector(QQ, X.row(j))

    n_found = sum(1 for v in phi0 if v is not None)
    if n_found < n_src:
        raise RuntimeError(
            f"L_0 orbit solver reached {n_found}/{n_src} fiber elements"
        )
    return phi0


def _apply_pbw_to_vec(M, v, start_deg, alpha, S):
    """
    Apply PBW monomial (\alpha, S) to v \in M[start_deg].

    PBW ordering: \partial_1^{\alpha_1}\partial_2^{\alpha_2}\partial_3^{\alpha_3}\partial_4^{\alpha_4} d_{s_1}...d_{s_k}
    Applied right-to-left: inner d's first, then outer \partial's.

    Returns result in M[start_deg + |\alpha| + |S|].
    """
    current = v
    deg = start_deg
    # Odd generators right-to-left: d_{s_k}, ..., d_{s_1}
    for s in sorted(S, reverse=True):
        mats = l_minus1_action_matrix(M, s, 1)
        current = mats[deg] * current
        deg += 1
    # Even generators inner-to-outer: \partial_4^{a_4}, ..., \partial_1^{a_1}
    for i in range(3, -1, -1):
        for _ in range(alpha[i]):
            mats = l_minus1_action_matrix(M, i, 0)
            current = mats[deg] * current
            deg += 1
    return current


def _phi_at_degree(M_source, M_target, sv_deg, phi0_vecs, source_deg):
    """
    Morphism matrix at source degree d: M_source[d] \to M_target[d + sv_deg].

    Key observation: _pbw_apply_even/_pbw_apply_odd each map one PBW monomial
    to exactly one PBW monomial with sign ±1 (never a sum).  Therefore the
    full PBW action of a source monomial (alpha_s, S_s) on any starting
    monomial at sv_deg can be computed by pure index arithmetic — no matrix
    arithmetic at all.

    Algorithm:
      1. Precompute pbw_action[(alpha_s, S_s)] = {j_sv: (j_result, sign)}
         for each source PBW monomial, mapping each sv_deg target monomial
         to its image (or absent if killed).
      2. Fill the phi matrix directly from phi0_vecs and the action maps.
    """
    n_tar = M_target.dim(source_deg + sv_deg)
    n_src = M_source.dim(source_deg)
    dim_W_src = M_source.dim_W
    dim_W_tar = M_target.dim_W

    if n_tar == 0 or n_src == 0:
        return matrix(QQ, n_tar, n_src, sparse=True)

    pbw_src  = M_source.pbw[source_deg]
    pbw_sv   = M_target.pbw[sv_deg]
    dest_deg = source_deg + sv_deg
    pbw_dest = M_target.pbw[dest_deg]
    pbw_dest_idx = {mon: j for j, mon in enumerate(pbw_dest)}

    from verma_modules import _pbw_apply_even, _pbw_apply_odd

    # Step 1: for each source PBW monomial, precompute the action on all
    # sv_deg target monomials (pure symbolic, zero matrix arithmetic).
    # action_maps[j_s] = list of (j_sv, j_result, sign)
    action_maps = []
    for j_s, (alpha_s, S_s) in enumerate(pbw_src):
        acts = []
        for j_sv, (alpha_sv, S_sv) in enumerate(pbw_sv):
            # Apply odd generators right-to-left
            cur_alpha, cur_S, cur_sign = alpha_sv, S_sv, QQ(1)
            ok = True
            for s in sorted(S_s, reverse=True):
                res = _pbw_apply_odd(cur_alpha, cur_S, s)
                if res is None:
                    ok = False
                    break
                cur_alpha, cur_S, cur_sign = res[0], res[1], cur_sign * res[2]
            if not ok:
                continue
            # Apply even generators inner-to-outer
            for i in range(3, -1, -1):
                for _ in range(alpha_s[i]):
                    cur_alpha, cur_S, sign_i = _pbw_apply_even(cur_alpha, cur_S, i)
                    cur_sign *= sign_i
            # Look up result
            dest_mon = (cur_alpha, cur_S)
            j_result = pbw_dest_idx.get(dest_mon)
            if j_result is not None:
                acts.append((j_sv, j_result, cur_sign))
        action_maps.append(acts)

    # Step 2: fill phi matrix entries
    # Column j_s * dim_W_src + k_src maps to rows in n_tar
    # phi0_vecs[k_src] is a vector in M_target[sv_deg] = n_sv_monomials × dim_W_tar
    entries = {}
    n_sv_monomials = len(pbw_sv)
    for j_s, acts in enumerate(action_maps):
        if not acts:
            continue
        flat_src_base = j_s * dim_W_src
        for j_sv, j_result, sign in acts:
            sv_row_base  = j_sv    * dim_W_tar
            dest_row_base = j_result * dim_W_tar
            for k_src in range(dim_W_src):
                v = phi0_vecs[k_src]
                for k_tar in range(dim_W_tar):
                    val = v[sv_row_base + k_tar]
                    if val:
                        ekey = (dest_row_base + k_tar, flat_src_base + k_src)
                        prev = entries.get(ekey)
                        entries[ekey] = (sign * val) if prev is None else prev + (sign * val)

    return matrix(QQ, n_tar, n_src, entries, sparse=True)



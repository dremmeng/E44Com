def _check_s37(e44_data=None, verbose=True):
    """
    S3.7 checkpoint: Export morphisms and verify roundtrip.

    (1) Exports morphism_data.pkl via save_morphisms().
    (2) Reloads via load_morphisms() and verifies:
        - All morphism keys present
        - Matrix dimensions consistent with module dimensions
        - phi0 vectors non-None
        - Singular vector entries match
    """
    if e44_data is None:
        if verbose:
            print("─" * 60)
            print("s3.7 checkpoint — Export morphisms"
                  " (SKIPPED: no e44_data)")
            print("─" * 60)
        return True

    filepath = 'morphism_data.pkl'
    all_pass = True
    n_pass = 0
    n_fail = 0

    def _check(name, got, expected):
        nonlocal all_pass, n_pass, n_fail
        ok = (got == expected)
        if ok:
            n_pass += 1
        else:
            n_fail += 1
            all_pass = False
            if verbose:
                print(f"  [FAIL] {name}: got {got!r}, expected {expected!r}")

    if verbose:
        print("─" * 60)
        print("s3.7 checkpoint — Export morphisms")
        print("─" * 60)

    # Step 1: export
    save_morphisms(filepath, e44_data, max_deg=4, verbose=verbose)

    # Step 2: reload
    if verbose:
        print("\n  Reloading and verifying roundtrip ...")
    loaded = load_morphisms(filepath)
    _check("has 'morphisms' key", 'morphisms' in loaded, True)
    _check("has 'singular_vectors' key", 'singular_vectors' in loaded, True)
    _check("max_deg", loaded.get('max_deg'), 4)

    # Step 3: verify morphisms
    morphisms = loaded.get('morphisms', {})
    expected_morph_keys = [
        'phi_1A_0_1', 'phi_1A_1_0', 'phi_1A_2_1',
        'phi_1B_1_1', 'phi_1B_2_1', 'phi_1B_2_2',
        'phi_1C_1',
        'phi_1D_1', 'phi_1D_2', 'phi_1D_3',
        'phi_1E',
        'phi_3F', 'phi_3G',
        'phi_4H_1', 'phi_4H_3',
    ]
    _check("morphism count", len(morphisms), len(expected_morph_keys))
    for key in expected_morph_keys:
        _check(f"{key} present", key in morphisms, True)

    for key, mdata in morphisms.items():
        sv_deg = mdata['sv_deg']
        for d, mat in mdata['matrices'].items():
            expected_rows = mdata['tar_dims'].get(d + sv_deg, 0)
            expected_cols = mdata['src_dims'].get(d, 0)
            _check(f"{key}[{d}] rows", mat.nrows(), expected_rows)
            _check(f"{key}[{d}] cols", mat.ncols(), expected_cols)
        # phi0: all entries non-None
        for i, v in enumerate(mdata['phi0']):
            _check(f"{key} phi0[{i}] non-None", v is not None, True)

    # Step 4: verify singular vectors
    sv_data = loaded.get('singular_vectors', {})
    expected_sv_keys = [
        'w1A_0_1', 'w1A_2_1', 'w1B_1_1', 'w1B_2_2',
        'w1C_1',
        'w1D_1', 'w1D_3', 'w1E',
        'w3F', 'w3G', 'w4H_1', 'w4H_3',
    ]
    _check("singular vector count", len(sv_data), len(expected_sv_keys))
    for key in expected_sv_keys:
        _check(f"{key} present", key in sv_data, True)
        if key in sv_data:
            v = sv_data[key]['vec']
            nnz = sum(1 for x in v if x != 0)
            _check(f"{key} nonzero entries > 0", nnz > 0, True)

    # Summary
    if verbose:
        print(f"\n  Roundtrip verification: {n_pass} pass, {n_fail} fail")
        if all_pass:
            print("  s3.7  ✓  ALL CHECKS PASSED")
        else:
            print("  s3.7  ✗  SOME CHECKS FAILED — see above")
        print("─" * 60)

    return all_pass



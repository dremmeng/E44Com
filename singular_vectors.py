    # ── (4) L_{-1} consistency ─────────────────────────────────────────────
    # \phi_1 column (g_j, e_k) should equal g_j \cdot \phi_0(e_k)
    phi1_mat = mats[1]
    n_lm1_fail = 0
    for gen_idx in range(4):
        for parity in [0, 1]:
            lm1_mats = l_minus1_action_matrix(M_tar, gen_idx, parity)
            for k in range(min(M_src.dim_W, 3)):  # sample 3 fiber elts
                expected = lm1_mats[1] * phi0[k]
                # Find the source PBW index for this generator
                if parity == 0:
                    alpha = [0, 0, 0, 0]
                    alpha[gen_idx] = 1
                    src_mon = (tuple(alpha), frozenset())
                else:
                    src_mon = ((0, 0, 0, 0), frozenset({gen_idx}))
                j_src = M_src._pbw_idx[1][src_mon]
                actual_col = phi1_mat.column(j_src * M_src.dim_W + k)
                if actual_col != expected:
                    n_lm1_fail += 1
    _check("L_{-1} consistency (sample): failures", n_lm1_fail, 0)

    # ── (5) Rank check ─────────────────────────────────────────────────────
    rank0 = phi0_mat.rank()
    _check("\phi_0 matrix has full column rank",
           rank0 == M_src.dim_W, True)


def _check_s35(e44_data=None, verbose=True):
    """
    S3.5 checkpoint: Verma module morphisms.

    Verifies:
    (1) \phi[1A](v_hw) == w[1A]: the degree-0 morphism matrix sends v_hw
        to the singular vector w[1A].
    (2) L_0 equivariance of \phi_0 for even L_0 generators (sl_4 part):
        l0^M[1] \cdot \phi_0(e_k) == \Sigma h_W[k',k] \cdot \phi_0(e_{k'})
    (3) L_1 annihilation: all \phi_0(e_k) lie in the L_1 kernel of M_target[1].
    (4) L_{-1} consistency: degree-1 morphism matrix matches L_{-1} action.
    (5) Rank check: morphism matrix at degree 0 has full column rank.
    """
    if e44_data is None:
        if verbose:
            print("─" * 60)
            print("s3.5 checkpoint — Morphisms (SKIPPED: no e44_data)")
            print("─" * 60)
        return True

    all_pass = True

    def _check(name, got, expected):
        nonlocal all_pass
        ok = (got == expected)
        status = "PASS" if ok else "FAIL"
        if verbose:
            print(f"  [{status}] {name}: got {got!r}, expected {expected!r}")
        if not ok:
            all_pass = False

    if verbose:
        print("─" * 60)
        print("s3.5 checkpoint — Verma module morphisms \phi[1A]-\phi[4H]")
        print("─" * 60)

    # ── \phi[1A] at t=0, a=1 ─────────────────────────────────────────────────
